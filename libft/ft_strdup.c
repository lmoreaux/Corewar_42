#include "./includes/libft.h"

char	*ft_strdup(const char *s1)
{
	int		length;
	int		i;
	char	*str;

	length = ft_strlen(s1);
	str = (char *)malloc(sizeof(char) * (length + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
